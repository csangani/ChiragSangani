<p>Galaxy is the intra-college cultural competition of IIT Kanpur. This website 
was an entry into the website design competition (and went on to win the 2nd 
prize). The design is wholly original, with inspirations from different myriad 
sources long forgotten. The website was designed with one purpose in mind - to 
bring out the essence of color and its diversity, which is a metaphor for the 
cultural diversity of India.</p>
<p>Technically, this website was one of the most complicated websites ever 
designed by me, with various javascript trickeries required to achieve the final 
result. The website was first designed in Photoshop; subsequently it was 
meticulously converted into hard-coded HTML, CSS and javascript. The website 
seems to be compatible with most modern browsers, thanks to the reliable jQuery 
framework that it uses.</p>
