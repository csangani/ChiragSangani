<p>The familiar looking website above happens to be &quot;Zune Design&quot;, on 
which you are currently browsing. Inspired by the incredible Metro UI language developed 
by Microsoft for its Zune software, this website attempts to mimick the feel of 
a Metro desktop application. With fluid graphics, dynamic backgrounds and clean 
typography, the design attempts to be alive as well as subdued simultaneously.</p>
<p>The design of the website was first completed in Photoshop, following which an 
HTML and PHP template was designed. Being the first AJAX website designed by me, 
a rudimentary framework was developed to seperate the logic, design and content. 
A long period of experimentation followed to obtain the perfect match of design 
aesthetics as well as functionality. All effects and animations are provided by 
the jQuery framework.</p>
